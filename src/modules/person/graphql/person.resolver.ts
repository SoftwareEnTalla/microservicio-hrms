  @Mutation(() => PersonResponse<Person>)
  async createOrUpdatePerson(
    @Args("data", { type: () => CreateOrUpdatePersonDto })
    data: CreateOrUpdatePersonDto
  ): Promise<PersonResponse<Person>> {
    if (data.id) {
      const existingPerson = await this.service.findById(data.id);
      if (existingPerson) {
        return this.commandBus.execute(
          new UpdatePersonCommand(data, {
            instance: data,
            metadata: {
              initiatedBy:
                (data.input as CreatePersonDto | UpdatePersonDto).createdBy ||
                'system',
              correlationId: data.id,
            },
          })
        );
      }
    }
    return this.commandBus.execute(
      new CreatePersonCommand(data, {
        instance: data,
        metadata: {
          initiatedBy:
            (data.input as CreatePersonDto | UpdatePersonDto).createdBy ||
            'system',
          correlationId: data.id || uuidv4(),
        },
      })
    );
  }


